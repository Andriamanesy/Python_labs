"""
Exercice 3 : Comparaison
Objectif : Utiliser les opérateurs de comparaison (==, !=, >, <, >=, <=).

Consigne :
Crée deux variables m = 10 et n = 15.
Affiche les résultats des comparaisons :
m == n
m != n
m > n
m < n
m >= n
m <= n
"""
m, n = 10, 15

print(f"La comparaison de {m} == {n} est ",  m == n)
print(f"La comparaison de {m} != {n} est ",  m != n)
print(f"La comparaison de {m} > {n} est ",  m > n)
print(f"La comparaison de {m} < {n} est ",  m < n)
print(f"La comparaison de {m} >= {n} est ",  m >= n)
print(f"La comparaison de {m} <= {n} est ",  m <= n)